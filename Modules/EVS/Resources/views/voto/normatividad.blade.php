							2o.</span></b></a><span style='font-size:9.0pt;font-family:"Arial","sans-serif";
							color:#7C7B7B'>&nbsp;El presente acuerdo rige a partir de la fecha de su
							publicación en el&nbsp;<i>Diario Oficial&nbsp;</i>y modifica el Capítulo XI del
							Acuerdo número&nbsp;0007&nbsp;de 2012.</span></p>

