							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>m)
							Promover el fortalecimiento de las relaciones de convivencia en la comunidad
							educativa, a través del diálogo, el buen trato, el respeto y la conciliación;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>n)
							Participar en la preparación y ejecución de las actividades de inducción
							pertenecientes al Grupo de Fomento del Bienestar y Liderazgo del Aprendiz o
							quien haga sus veces, que programe el Centro de Formación, para los nuevos
							aprendices.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Artículo&nbsp;42.&nbsp;</span></i></b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>En caso
							de requerir representatividad en eventos simultáneos que requieran la presencia
							del Representante de Aprendiz Sena, se designará el suplente para que asista.
							Para tal caso, se redactará un acta de mutuo acuerdo firmada por el Subdirector
							del Centro y Líder del Grupo de Bienestar al Aprendiz o quien haga sus veces.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Artículo&nbsp;43. Procedimiento para elegir Representante de
							Aprendiz Sena</span></i></b><i><span style='font-size:9.0pt;font-family:"Arial","sans-serif";
							color:#7C7B7B'>:</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>a)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Dependencia
							responsable</span></i></b><i><span style='font-size:9.0pt;font-family:"Arial","sans-serif";
							color:#7C7B7B'>: El grupo de Fomento del Bienestar y Liderazgo del Aprendiz de
							la Dirección General o quien haga sus veces, elaborará el cronograma y abrirá
							la convocatoria respectiva, mediante acto administrativo emitido por la
							Dirección de Formación Profesional, a través del cual se convoca la elección
							del Representante de Aprendiz Sena por jornadas en Centro de Formación;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>b)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Inscripción
							de candidatos:&nbsp;</span></i></b><i><span style='font-size:9.0pt;font-family:
							"Arial","sans-serif";color:#7C7B7B'>Los aprendices interesados en postularse a
							la elección de Representante, pueden inscribirse en la convocatoria de
							elección, en forma individual, inscribiéndose a través de los canales de
							comunicación previstos y presentando la propuesta programática de trabajo para
							el periodo anual de representación;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>c)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Verificación
							de cumplimiento de requisitos</span></i></b><i><span style='font-size:9.0pt;
							font-family:"Arial","sans-serif";color:#7C7B7B'>. El Grupo de Fomento del
							Bienestar y Liderazgo del Aprendiz del Centro de Formación, o quien haga sus
							veces, con apoyo de la Dirección General verificará el cumplimiento de los
							requisitos de los aprendices inscritos durante la semana posterior a la
							inscripción de la candidatura;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>d)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Divulgación
							de los resultados.&nbsp;</span></i></b><i><span style='font-size:9.0pt;
							font-family:"Arial","sans-serif";color:#7C7B7B'>El área de Fomento del
							Bienestar y Liderazgo del Aprendiz en cada Centro de Formación o quien haga sus
							veces, publicará los resultados de la verificación de los requisitos de los
							candidatos inscritos;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>e)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Participación</span></i></b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>. Para
							el proceso de elección del Representante de Aprendiz Sena deberá participar
							mínimo el 50% de los aprendices de formación titulada del Centro de Formación;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>f)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Votación.&nbsp;</span></i></b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>La
							votación se realizará el segundo lunes de marzo de cada año, así: Jornada
							diurna y 24 horas de 5:00 a. m. a 10 a. m.; jornada mixta y nocturna de 2 p. m.
							a 7 p. m. Para la jornada fin de semana las votaciones se realizarán el domingo
							anterior a la fecha de elecciones indicada, de 11 a. m. a 2 p. m. Las
							votaciones se harán en urnas selladas o mediante el uso de herramientas
							digitales con su correspondiente auditoría. Serán parte del grupo auditor un
							delegado del Grupo de Fomento del Bienestar y Liderazgo del Aprendiz del Centro
							de Formación, un aprendiz Sena y un delegado de cada candidatura siempre y
							cuando no esté postulado al cargo de Representante del Centro;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>g)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Escrutinio</span></i></b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>. El
							Grupo de Fomento del Bienestar y Liderazgo del Aprendiz de cada Centro de
							Formación o quien haga sus veces, hará parte de la comisión escrutadora siendo
							garante y verificador del proceso. El escrutinio se realizará el día de cierre
							de la votación. Se levantará acta debidamente suscrita por los integrantes de
							la comisión escrutadora. La Comisión estará conformada por un vocero de
							programa de formación, un Aprendiz elegido al azar, un Instructor, un
							Coordinador Académico y un representante del área de Bienestar a los
							Aprendices;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>h)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Elección.&nbsp;</span></i></b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>Se
							declararán ganadores a quienes obtengan la mayoría simple de votos. En caso de
							existir empate, la Comisión Escrutadora definirá el ganador por sorteo en
							presencia de los integrantes de las mismas. Se expedirá acta según formato
							establecido y firmada por el Representante de Aprendiz Sena;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>i)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Publicación
							del resultado electoral.&nbsp;</span></i></b><i><span style='font-size:9.0pt;
							font-family:"Arial","sans-serif";color:#7C7B7B'>El resultado de la elección se
							informará oficialmente por escrito a los candidatos, recordándoles los
							compromisos asumidos y las responsabilidades que esta designación conlleva. De
							igual forma, se publicará el resultado en las carteleras del Centro de
							Formación, informando a la Comunidad Educativa el resultado de la votación;</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>j)&nbsp;</span></i><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Designación.&nbsp;</span></i></b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>Se
							realizará el acto protocolario el viernes siguiente a la elección, con la
							expedición de la Resolución firmada por el Subdirector del Centro y ante la
							Comunidad Educativa del Centro de Formación.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Parágrafo
							1o.&nbsp;</span></i></b><i><span style='font-size:9.0pt;font-family:"Arial","sans-serif";
							color:#7C7B7B'>Si en el escrutinio el voto en blanco supera el 50% más 1 de los
							votos válidos, debe realizarse una nueva elección, la cual será convocada al
							siguiente lunes de la fecha establecida (tercer lunes de marzo). Los candidatos
							inscritos podrán mantener su postulación o retirarse, siempre y cuando cumplan
							con los requisitos preestablecidos.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Parágrafo
							2o.&nbsp;</span></i></b><i><span style='font-size:9.0pt;font-family:"Arial","sans-serif";
							color:#7C7B7B'>Cuando el Centro de Formación presente condiciones atípicas que
							impidan la elección del Representante de Aprendiz Sena en la fecha estipulada,
							las elecciones deberán postergarse hasta que el Centro de Formación supere las
							condiciones atípicas. Se deberá tener en cuenta un cronograma de por lo menos
							tres (3) semanas para realizar el proceso electoral.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Parágrafo
							3o.&nbsp;</span></i></b><i><span style='font-size:9.0pt;font-family:"Arial","sans-serif";
							color:#7C7B7B'>En caso de que la Jornada del Centro de Formación no presente
							candidatos inscritos para la elección del Representante de Aprendiz Sena, este
							será escogido en reunión de voceros de la jornada correspondiente quienes
							postularán tres candidatos, entre ellos se hará una votación cerrada con la
							participación del Subdirector de Centro y el Líder de Fomento del Bienestar y
							Liderazgo del Aprendiz del Centro. Se deberá llevar estricto registro por medio
							de acta del desarrollo de la reunión.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>Los
							candidatos deberán cumplir las mismas condiciones estipuladas en el artículo&nbsp;38&nbsp;y&nbsp;40&nbsp;para ser Representante de Aprendiz
							Sena.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Artículo&nbsp;44. Revocatoria de la designación del
							Representante de Aprendiz Sena.&nbsp;</span></i></b><i><span style='font-size:
							9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>El Representante de
							Aprendiz Sena puede ser removido de su representación perdiendo su investidura
							y representatividad ante la comunidad educativa del Centro de Formación, por
							las siguientes causales:</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>a)
							Incumplimiento de sus responsabilidades como Representante de Aprendiz Sena;</span></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>b) Bajo
							rendimiento académico que amerite mayor dedicación a su formación;</span></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>c)
							Sanciones recibidas por aspectos académicos o disciplinarios dictaminado por el
							comité de evaluación y seguimiento de conformidad con lo señalado en el
							procedimiento para la aplicación de sanciones del reglamento del aprendiz;</span></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>d)
							Solicitud sustentada de la comunidad educativa;</span></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>e)
							Terminación anticipada de su proceso de formación en el Sena;</span></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>f)
							Inicio de etapa productiva.</span></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Parágrafo
							1o.&nbsp;</span></i></b><i><span style='font-size:9.0pt;font-family:"Arial","sans-serif";
							color:#7C7B7B'>Para la revocatoria de la designación como Representante de
							Aprendiz Sena el comité de evaluación y seguimiento se reunirá con el
							Representante del Aprendiz Sena, el coordinador académico y el Subdirector del Centro,
							analizarán la situación y tomarán las determinaciones que correspondan.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Parágrafo
							2o.&nbsp;</span></i></b><i><span style='font-size:9.0pt;font-family:"Arial","sans-serif";
							color:#7C7B7B'>Cuando opere la revocatoria para uno de los representantes
							asumirá el cargo el Suplente.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Parágrafo
							3o.&nbsp;</span></i></b><i><span style='font-size:9.0pt;font-family:"Arial","sans-serif";
							color:#7C7B7B'>El acto administrativo de revocatoria de la Representación de
							Aprendiz Sena no es susceptible de recursos en razón a que son actos
							académicos.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Artículo&nbsp;45.&nbsp;</span></i></b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>&lt;sic&gt;
							En caso de que el Representante de Aprendiz Sena y el suplente salgan al mismo
							tiempo a etapa productiva o por alguna otra circunstancia se dé la vacancia en
							la representación, asumirá quien haya obtenido el tercer lugar en la elección y
							así sucesivamente en orden descendente. El escogido deberá continuar gestionando
							la propuesta programática del Representante elegido inicialmente por votación y
							cumplir con las funciones señaladas en el artículo&nbsp;41&nbsp;del reglamento.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Artículo&nbsp;46. &lt;sic&gt; Periodos de Representación.&nbsp;</span></i></b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>Los
							períodos son de carácter institucional y no personal. Por consiguiente, en caso
							de producirse la vacancia en la Representación del Aprendiz Sena, por cualquier
							causa, el nuevo Representante será el suplente durante el tiempo faltante para
							concluir el período o el tiempo que dure la ausencia del Representante.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#008400'>Artículo&nbsp;47. &lt;sic&gt; Presentación y difusión de
							propuestas de trabajo ante la Comunidad Educativa</span></i></b><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>. El
							Centro de Formación, a través del subdirector y con apoyo del Grupo de Fomento
							del Bienestar y Liderazgo del Aprendiz del Centro o quien haga sus veces,
							organizará sesiones para que los candidatos inscritos presenten su propuesta de
							trabajo ante la comunidad educativa, convocando a todos los aprendices del
							Centro de Formación.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>Se
							garantizará la divulgación de las propuestas de los candidatos en igualdad de
							condiciones, durante quince días calendario previos a las elecciones mediante
							los siguientes mecanismos:</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>1.
							Medios impresos, usando únicamente las carteleras informativas con las que
							cuente en centro de formación.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>2.
							Medios electrónicos, como correo electrónico y las pantallas informativas de
							los centros.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>3. Un
							foro de participación al que se invite a toda la comunidad de aprendices y en
							donde los aspirantes a representantes debatan sus propuestas en público.</span></i></p>

							<p class=MsoNormal style='text-align:justify;line-height:normal'><i><span
							style='font-size:9.0pt;font-family:"Arial","sans-serif";color:#7C7B7B'>4. Una
							videoconferencia establecida previamente por el centro de formación.</span></i></p>


